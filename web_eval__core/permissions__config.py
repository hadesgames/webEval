## Wiki

### View any wiki page
WIKI_VIEW_ALL = 'wiki-view:all'

### Edit all wiki pages
WIKI_EDIT_ALL = 'wiki-edit:all'

### Delete own wiki pages
WIKI_DELETE_OWN = 'wiki-delete:own'

### Delete any wiki page
WIKI_DELETE_ALL = 'wiki-delete:all'

### Move own wiki pages
WIKI_MOVE_OWN = 'wiki-move:own'

### Move any wiki page
WIKI_MOVE_ALL = 'wiki-move:all'


### Attach files on own wiki pages
WIKI_ATTACH_OWN = 'wiki-attach:own'

### Attach files  on all wiki pages
WIKI_ATTACH_ALL = 'wiki-attach:all'

